package com.example.cleanerappv1.model;

public class Cleaner {
    private String age;
    private String basicHouseKeeping;
    private String carCleaning;
    private String contactNumber;
    private String emailAddress;
    private String fullName;
    private String gender;
    private String image;
    private String moveInOutCleaning;
    private String officeCommercialCleaning;
    private String password;
    private String springCleaning;
    private String uid;
    private String userType;
    private String username;
    private String yearExperience;

    public Cleaner(){

    }

    public Cleaner(String age, String basicHouseKeeping, String carCleaning, String contactNumber, String emailAddress, String fullName, String gender, String image, String moveInOutCleaning, String officeCommercialCleaning, String password, String springCleaning, String uid, String userType, String username, String yearExperience) {
        this.age = age;
        this.basicHouseKeeping = basicHouseKeeping;
        this.carCleaning = carCleaning;
        this.contactNumber = contactNumber;
        this.emailAddress = emailAddress;
        this.fullName = fullName;
        this.gender = gender;
        this.image = image;
        this.moveInOutCleaning = moveInOutCleaning;
        this.officeCommercialCleaning = officeCommercialCleaning;
        this.password = password;
        this.springCleaning = springCleaning;
        this.uid = uid;
        this.userType = userType;
        this.username = username;
        this.yearExperience = yearExperience;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getBasicHouseKeeping() {
        return basicHouseKeeping;
    }

    public void setBasicHouseKeeping(String basicHouseKeeping) {
        this.basicHouseKeeping = basicHouseKeeping;
    }

    public String getCarCleaning() {
        return carCleaning;
    }

    public void setCarCleaning(String carCleaning) {
        this.carCleaning = carCleaning;
    }

    public String getContactNumber() {
        return contactNumber;
    }

    public void setContactNumber(String contactNumber) {
        this.contactNumber = contactNumber;
    }

    public String getEmailAddress() {
        return emailAddress;
    }

    public void setEmailAddress(String emailAddress) {
        this.emailAddress = emailAddress;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getMoveInOutCleaning() {
        return moveInOutCleaning;
    }

    public void setMoveInOutCleaning(String moveInOutCleaning) {
        this.moveInOutCleaning = moveInOutCleaning;
    }

    public String getOfficeCommercialCleaning() {
        return officeCommercialCleaning;
    }

    public void setOfficeCommercialCleaning(String officeCommercialCleaning) {
        this.officeCommercialCleaning = officeCommercialCleaning;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSpringCleaning() {
        return springCleaning;
    }

    public void setSpringCleaning(String springCleaning) {
        this.springCleaning = springCleaning;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getYearExperience() {
        return yearExperience;
    }

    public void setYearExperience(String yearExperience) {
        this.yearExperience = yearExperience;
    }
}
